function fmt(n){ return (+n).toLocaleString('pt-BR',{minimumFractionDigits:2}); }
function disc(orig,sale){ return orig>0&&sale>0?Math.round((1-(sale/orig))*100):0; }
function stars(n){ return '★'.repeat(Math.floor(n))+(n%1>=.5?'½':'')+'☆'.repeat(5-Math.ceil(n)); }

function renderProducts(){
  // Destaque principal (primeiro produto)
  const p0 = DVNET_PRODUCTS[0];
  if(!p0) return;
  const destEl = document.querySelector('.flash-product, [data-dvnet="destaque"], #destaque-produto');
  if(destEl){ destEl.innerHTML = buildDestaque(p0); }

  // Grid principal de produtos
  const gridEl = document.querySelector('#products .products-grid, #products .grid, [data-dvnet="products-grid"]');
  if(gridEl){ gridEl.innerHTML = DVNET_PRODUCTS.slice(0,5).map(buildCard).join(''); return; }

  // Fallback: inject after section heading
  const sec = document.getElementById('products') || document.querySelector('section[id*="product"]');
  if(sec){
    let grid = sec.querySelector('.dvnet-injected');
    if(!grid){ grid=document.createElement('div'); grid.className='dvnet-injected'; grid.style.cssText='display:grid;grid-template-columns:repeat(auto-fill,minmax(260px,1fr));gap:20px;padding:0 5%'; sec.appendChild(grid); }
    grid.innerHTML = DVNET_PRODUCTS.map(buildCard).join('');
  }
}

